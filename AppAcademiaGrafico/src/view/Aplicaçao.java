    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
       
        if(jTextFieldNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo 'Nome' não foi preenchido","Aviso", 0);
            jTextFieldNome.setText(null);
            jTextFieldNome.requestFocusInWindow();
            return;
        }
        
        if(jTextFieldSobrenome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo 'Sobrenome' não foi preenchido", "Aviso", 0);
            jTextFieldSobrenome.setText(null);
            jTextFieldSobrenome.requestFocusInWindow();
            return;
        }
        
        if(jTextFieldPeso.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo 'Peso' não foi preenchido", "Aviso", 0);
            jTextFieldPeso.setText(null);
            jTextFieldPeso.requestFocusInWindow();
            return;
        }
        
        if(jTextFieldAltura.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo 'Altura' não foi preenchido", "Aviso", 0);
            jTextFieldAltura.setText(null);
            jTextFieldAltura.requestFocusInWindow();
            return;
        }
        
        double peso = Double.parseDouble(jTextFieldPeso.getText().trim());
        double altura = Double.parseDouble(jTextFieldAltura.getText().trim());
        
        objAtleta = new Atleta(altura,peso,jTextFieldNome.getText().trim(), jTextFieldSobrenome.getText().trim());
        
        jTextAreaResultado.setText(objAtleta.toString());


    }//GEN-LAST:event_jButtonCalcularActionPerformed

